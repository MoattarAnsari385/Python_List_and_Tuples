a = (1, 2, 3, 4, 5)
print(type(a)) # <class 'tuple'>
print(a)

b = (1)
print(type(b))
print(b) # <class 'int'>

c = (1,)
print(type(c))
print(c) # <class 'tuple'>

d = ()
print(d)

e = (1, 2, 3, 4, 5, 6, 7, 8)
e[0] = 2
print(e) # Tuple is Immutable we can't change it
































